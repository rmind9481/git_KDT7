# ----------------------------------------------------------------
# [문] 'hello' 문자열을 코드값으로 출력하세요.
# - 코드값 : ord("H")
# ----------------------------------------------------------------


data = 'hello'
# 원소 읽기
for i in data:
    
    print(ord(i)) 

# ---------------------------------------------------------------
# [문] ['A','C','E'] 를 코드값을 저장하도록 해주세요.
# - 코드값 : ord('A')
# ---------------------------------------------------------------

data = ['A','C','E']

# 원소 값 변경하기
for idx in range(len(data)):
    data[idx] = ord(data[idx])
    print(data) 

