# -----------------------------------------------------------------------
#   list 자료형
#   - 다양한 종류의 여러 개의 데이터를 저장하는 타입
#   - 형         식 : [ 데이터1, 데이터2, ....., 데이터n]
#   - 원소/요소 식별 : 인덱스 => 슬라이싱
