#- 데이터

print(f"나이 : {10}")
print('a')

print(f'10의 데이터 종류/타입 : {type(10)}')
print(f'10.의 데이터 종류/타입 : {type(10.)}')
print(f'"10."의 데이터 종류/타입 : {type("10.")}')
print(f'"10"의 데이터 종류/타입 : {type("10")}')

print(f"False의 데이터 종류/타입 : {type(False)}")