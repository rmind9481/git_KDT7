#  모듈 로딩 -----------------------------------------------------------------------
import pandas  as pd

# 전역변수 -------------------------------------------------------------------------
data = {'홍':[17,'남','덕영고'],
        '마':[15,'여','대구중']}


# 데이터 프레임
dataDF = pd.DataFrame(data)


# 데이터 프레임 만들기 -------------------------------
# - Dict 타입의 데이터 => DataFrame 생성
# - Key 가 컬럼식별자로 설정
# - Value 가 세로로 즉, 컬럼값으로 설정
# ---------------------------------------------------
#         |  홍   |   마   |
#     0   |  17   |   15   |
#     1   |  남   |   여   |
#     2   | 덕영고 | 대구중 |

print('DataDF---\n',dataDF) 